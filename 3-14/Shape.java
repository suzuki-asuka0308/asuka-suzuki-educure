public abstract class Shape {
       //abstract がついてる → これは 抽象クラス
    //意味：このクラスは 直接使う（new Shape()）ことはできない
    //子クラスに継承させて、ルールや共通点だけをまとめるためのクラス！
    public abstract double getArea();  // 図形の面積を計算するルール
    //抽象メソッド。中身（処理の中）はまだ書いてない。
    //double 型 → 「面積は少数になるかも」という想定（例：78.5とか）
    //子クラス（例：Circle や Rectangle）が必ずこのメソッドをオーバーライドして使うことになる！
}

/*Shape クラスは “図形の型” を作るルール帳。
getArea() は “面積を出す” という共通の命令だけ決めておいて、
実際にどう計算するかは図形ごと（子クラス）にまかせる */

