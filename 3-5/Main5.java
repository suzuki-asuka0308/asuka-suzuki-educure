public class Main5 {
    public static void main(String[] args) {
        // 【本1】タイトルと著者のみ（ページ数は0）
        Book book1 = new Book("Java入門", "山田太郎");
        //Book クラスのインスタンスを作り、それを book1 や book2 に保存する
        //ポイント：「クラスから実際のデータを作るには new を使う！」
        //book1 は タイトルと著者だけを指定（ページ数は0）
        //book1 は「タイトルと著者」だけ → pages = 0 に初期化

        // 【本2】タイトル、著者、ページ数を指定
        Book book2 = new Book("Python実践", "鈴木次郎", 300);
        //book2 は タイトル・著者・ページ数をすべて指定
        //new を使うと、クラスのコンストラクタが実行される！
        //new を使うとクラスからオブジェクトが作られる！
        //new Book(...) の引数の違いで、どのコンストラクタが動くか決まる
        //ポイント：「オーバーロードを使うと、初期化の仕方を変えられる！」
        //book2 は「タイトル・著者・ページ数」をすべて指定 → pages = 300
        //オーバーロードを使うと、コンストラクタを使い分けられる！

        // 本の情報を表示
        book1.displayInfo();
        book2.displayInfo();
        //displayInfo() を呼ぶと、本の情報が表示される！
        //ポイント：「メソッドを作ることで、何度でもデータを出力できる！」
        //book1.displayInfo(); → 「Java入門」の情報を表示
        //book2.displayInfo(); → 「Python実践」の情報を表示
        //メソッドを作ると、オブジェクトの情報を簡単に表示できる！
        //book1 と book2 の違いは、使ったコンストラクタが違うこと
        
    }
}