public class Monster {
    // 攻撃メソッド（オーバーライドされる）
    public void attack() {
        //このメソッドは、Monster クラスの基本攻撃を表す。
        //このままだと、Monster の攻撃はいつも「モンスターの攻撃！」になる。
        //Slime クラス（子クラス）でオーバーライドされる！
        //親クラスの attack() は、基本の攻撃動作を持っている！
        //public をつけることで、他のクラス（Slime など）からも呼び出せる
        //もし private にすると、Slime クラスで attack() をオーバーライドできなくなる！
        //子クラスでオーバーライドするために、public にしておく！
        System.out.println("モンスターの攻撃！");

    }
}

/*項目
覚えること
attack() メソッドの役割
基本の攻撃を定義し、子クラスでオーバーライドできる
public の意味
他のクラスから呼び出せるようにする
オーバーライド可能
Slime で attack() を上書きできる
 */

 //attack() は基本攻撃の処理！
 //public にすることで、子クラスでオーバーライドできる！
 //Monster クラスの attack() をそのまま使うこともできる！